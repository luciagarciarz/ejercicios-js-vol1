/* 
1. Declara una variable llamada nota1 con un valor numérico (por ejemplo, 7).
2. Declara una segunda variable nota2.
3. Calcula el promedio de ambas notas y guárdalo en una variable promedio.
4. Usa la sentencia if para mostrar en consola:
    - "Aprobado" si el promedio es mayor o igual a 5

    - "Suspenso" si el promedio es menor a 5
*/

let nota1 = 6
let nota2 = 4
let media = (nota1 + nota2) / 2

if (media >= 5) {
    console.log("Aprobado")
}
else {
    console.log("Suspenso")
}

